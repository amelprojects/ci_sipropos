			<p>Proyek pendanaan COMCEC atau yang lebih dikenal dengan COMCEC Project Funding (CPF) merupakan mekanisme pembiayaan yang diperkenalkan oleh Kantor Koordinasi COMCEC atau COMCEC Coordination Office (CCO) pada tahun 2013. Proyek akan mendanai proposal yang diajukan oleh negara anggota dan institusi relevan di dalam Organisasi Kerja Sama Islam (Organization of Islamic Cooperation/OIC) untuk mewujudkan strategi objektif dan hasil dari Strategi COMCEC. CPF bertujuan untuk merealisasikan rekomendasi-rekomendasi kebijakan yang diadopsi oleh Sesi Pertemuan Menteri COMCEC/COMCEC Ministerial Sessions, meningkatkan solidaritas dan kerja sama multilateral di antara negara-negara anggota seiring dengan meningkatkan kapasitas institusi dan sumber daya manusia di negara-negara tersebut.</p><br>

            <p>Institusi publik yang relevan di negara-negara anggota COMCEC yang merupakan anggota dari Kelompok Kerja (Working Group) dan Institusi-institusi yang terafiliasi dalam OKI yang beroperasi di bidang ekonomi dan kerja sama komersial dapat mengajukan proposal proyek. Proyek dapat didesain dalam 6 (enam) area kerja sama yang berbeda (agrikultur, keuangan, pengentasan kemiskinan, transportasi dan komunikasi, pariwisata dan perdagangan) dan sejalan dengan tema sectoral yang telah ditentukan oleh CCO dan diumumkan melalui website.</p>

            <img class="aligncenter wp-image-3517 tie-appear" src="./assets/images/pcm_en.png" sizes="(max-width: 570px) 100vw, 570px" width="570" height="452"></p>

            <p>Proyek yang didanai memiliki 2 (dua) tipe yaitu proyek yang berorientasi pada pelaksanaan suatu aktivitas (activity based) dan proyek yang didasarkan untuk kepentingan penelitian (research based). Proyek dengan tipe pertama dapat memasukan berbagai aktivitas seperti pelatihan, workshop, seminar, konferensi, studi lapangan dan aktivitas-aktivitas promosi, dll. Sedangkan untuk proyek tipe kedua dapat terdiri dari persiapan studi dan penelitian, dokumen-dokumen kebijakan, studi kelayakan dan pelaporan terkait studi lapangan dalam konteks tersebut.</p>
            
            <p>Kantor Koordinasi COMCEC/COMCEC Coordination Office (CCO) membuka penerimaan pengajuan proposal setiap tahunnya pada bulan September melalui website COMCEC. Pemilik proyek akan mengumpulkan proposalnya melalui Sistem Pengajuan Proyek Online dalam 2 (dua) tahap. Pada tahap pre-eliminasi, informasi-informasi umum mengenai proyek dan aktivitas-aktivitas yang diajukan perlu disiapkan. Setelah penilaian awal oleh CCO dan tim penilai independen, daftar awal proyek yang dipilih akan diumumkan. Para pemilik proyek tersebut akan melengkapi proposalnya ke tahap final dengan menyediakan detil informasi mengenai aktivitas, sumber daya manusia, budget dan isu-isu relevan lainnya. Untuk proyek yang berorientasi penelitian, pemilik proyek perlu memasukan dokumen kerangka kerja/ Terms of Reference yang akan menjelaskan detil studi penelitian yang telah dicantumkan pada tahap pre-eliminasi. Apabila proyek penelitian terpilih maka pemilik proyek perlu menggabungkan setidaknya 3 (tiga) penawaran finansial dan teknis untuk keperluan TOR tersebut dan mengumpulkannya pada tahap final. Pengumpulan proyek akan diproses melalui focal point dari masing-masing kelompok kerja COMCEC yang memiliki username dan password untuk Sistem Pengajuan Proyek Online. Durasi maksimum untuk proyek adalah selama 7 (tujuh) bulan , biasanya dimulai pada tanggal 1 April di tahun tersebut.</p>

            <p><img class="aligncenter wp-image-3518" src="./assets/images/pcm2_en.png" sizes="(max-width: 645px) 100vw, 645px" alt="" width="645" height="320"></p>

            <p>CCO mempertimbangkan kriteria utama di bawah ini dalam melakukan evaluasi proposal proyek. Pemilik proyek  perlu memperhatikan masing-maisng kriteria agar pendanaan proposal proyek dapat terpilih.</p>
            <ul style="color: #1a6f64;">
                <li>Relevansi dengan Strategi COMCEC</li>
                <li>Keserasian terhadap Rekomendasi Kebijakan Tingkat Menteri COMCEC/ COMCEC Ministerial Policy Recommendations dan Tema Sektoral yang diumumkan melalui website COMCEC</li>
                <li>Promosi kerja sama multilateral antarnegara anggota COMCEC</li>
                <li>Partisipasi regular dari focal point terhadap pertemuan kelompok kerja COMCEC</li>
                <li>Pengajuan dan pengisian proposal yang sesuai dengan aturan yang ditentukan</li>
                <li>Kualifikasi dari personil proyek yang diajukan</li>
                <li>Estimasi pendanaan yang realistis dalam proyek</li>
                <li>Kerja sama dan komunikasi dengan CCO</li>
                <li>Keterkaitan dengan proyek yang telah didanai COMCEC sebelumnya dan yang telah diimplementasikan</li>
            </ul>
            <p>&nbsp;</p>

            <p>The Development Bank of Turkey merupakan salah satu badan utama dalam CPF yang akan melakukan transfer dana kepada Pemilik Proyek dan juga sekaligus mengawasi implementasi proyek-proyek yang disetujui. Pemilik Proyek akan menandatangani kontrak dengan Bank terkait peran detil, hak dan tanggung jawab dari semua pihak yang terlibat dalam CPF. Pemilik Proyek perlu membuat laporan rutin kepada CCO dan Bank selama implementasi proyek. CPF merupakan instrument pendukung keuangan berbasis hibah yang menyediakan dana hingga USD 225,000 dan USD 75,000 untuk proyek yang diajukan oleh negara-negara anggota OKI dan institusi-institusi relevan di dalam OKI. Pembayaran akan dilakukan oleh Bank setelah pengeluaran terjadi dan dilaporkan kepada Bank dengan menyerahkan dokumen-dokumen pendukung yang relevan. Untuk proyek berbasis penelitian, pembayaran akan dilakukan dalam 2 (dua) tahap; pertama,  setelah draf riset awal disetujui oleh CCO dan yang kedua setelah laporan riset disetujui.</p>

